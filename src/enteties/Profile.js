class Profile {
    
    constructor(email){
        this.email = email;
    }

    setEmail(email){
        this.email = email;
    }

    getEmail(){
        return this.email;
    }

    setFirstName(firstName){
        this.firstName = firstName;
    }

    getFirstName(){
        return this.firstName;
    }

    setLastName(lastName){
        this.lastName = lastName;
    }
    
}

